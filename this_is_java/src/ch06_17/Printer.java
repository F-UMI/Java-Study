package ch06_17;
//@override 실습
public class Printer {
	public static void println(int n) {
		System.out.println(n);
	}

	public static void println(boolean n) {
		System.out.println(n);
	}

	public static void println(double n) {
		System.out.println(n);
	}

	public static void println(String n) {
		System.out.println(n);
	}
}

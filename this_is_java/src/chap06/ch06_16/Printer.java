
package chap06.ch06_16;

//@override 실습
public class Printer {
	public void println(int n) {
		System.out.println(n);
	}

	public void println(boolean n) {
		System.out.println(n);
	}

	public void println(double n) {
		System.out.println(n);
	}

	public void println(String n) {
		System.out.println(n);
	}
	
}
